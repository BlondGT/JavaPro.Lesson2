package ua.ithillel.lesson2.part1;

public class Employee {

    public String name;
    public String position;
    public String email;
    public String phone;
    public int age;


    public Employee(String name, String position, String email, String phone, int age) {
        this.name = name;
        this.position = position;
        this.email = email;
        this.phone = phone;
        this.age = age;
    }

}
